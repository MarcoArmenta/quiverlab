"""Bardzell's minimal projective A^e-bimodule resolution for MONOMIAL algebras.

A monomial algebra A = kQ/I (I generated by paths/monomials) admits the minimal
projective bimodule resolution of Bardzell (1997, "The alternating syndrome of a
monomial algebra"; see also Skoldberg, and Redondo--Roman). This module exposes it
as a homology-only `Resolution` backend, `BardzellResolution`, cross-checked exactly
against the normalized-bar-complex oracle (hanlab.homology_dims) and reaching depths
far past the bar complex's combinatorial blow-up.

------------------------------------------------------------------------------
THE MATH (what is implemented)
------------------------------------------------------------------------------
Let A = kQ/I be monomial with vertex set Q_0, arrow set Q_1, and minimal generating
set R of relations (each a path of length >= 2). Bardzell's "associated paths"
(equivalently Anick's n-chains / n-ambiguities) are graded sets AP^n:

  * AP^0 = Q_0 (vertices)
  * AP^1 = Q_1 (arrows)
  * AP^2 = R   (the minimal relations)
  * AP^n (n >= 2): a path p admits a LEFT DECOMPOSITION  p = u_0 u_1 ... u_{n-1}
    into n nonempty blocks with u_0 an arrow, such that for every consecutive pair,
        u_{k} u_{k+1} in I   and   no proper suffix of u_k u_{k+1} lies in I,
    i.e. u_k u_{k+1} is exactly a minimal relation. This is Bardzell's overlap/chain
    condition: each relation overlaps the next, the chain covering p exactly. For
    these algebras the decomposition is unique.

The projective bimodule resolution is
        P_n = (+)_{p in AP^n}  A e_{o(p)} (x) e_{t(p)} A,
where o(p) = source(p), t(p) = target(p). Applying  A (x)_{A^e} -  collapses each
summand to e_{o(p)} A e_{t(p)} = { paths w : t(p) -> o(p) in A } ("loops closing p").
So the n-th term of the reduced complex has basis

        C_n = { (p, w) : p in AP^n,  w a nonzero path  t(p) -> o(p) in A }.

THE REDUCED DIFFERENTIAL d_n : C_n -> C_{n-1} alternates (Bardzell's alternating
formula, pushed through A (x)_{A^e} -):

  n == 1  (arrows -> vertices):
        d_1(x, w) = (t(x); w.x)  -  (o(x); x.w)
     (the commutator/augmentation map; on a single loop these cancel iff the loop
      commutes with the arrow -- giving HH_0 = A/[A,A]).

  n even >= 2  ("big" map): for each (n-1)-associated path q occurring as a subpath
        p = pre . q . suf,
        d_n(p, w)  +=  ( q ;  suf . w . pre )       (summed over all occurrences).

  n odd >= 3  ("small" map): with the unique decomposition p = u_0 u_1 ... u_{n-1},
        P = u_1 ... u_{n-1}   (drop first block; p = u_0 . P),
        Q = u_0 ... u_{n-2}   (drop last block;  p = Q . u_{n-1}),
        d_n(p, w)  =  ( P ;  w . u_0 )  -  ( Q ;  u_{n-1} . w ).

Loop composition is in path order and the result is reduced mod I (a product that
runs through a relation, or fails to compose, is zero). Integer entries are kept
UN-REDUCED mod p, so small primes (e.g. p | something in the boundary) carry signal.

------------------------------------------------------------------------------
VERIFIED (exact agreement with the bar-complex oracle, primes 32003,2,3,5)
------------------------------------------------------------------------------
  * k[x]/(x^a)         (one vertex, one loop x, relation x^a)      a in {2,3,4,...}
  * cyclic Nakayama    kZ_n / rad^ell (n-cycle, length-ell relations)
  * local_3gen_radsq   (one vertex, 3 loops, all length-2 relations) -- the richest
                        monomial test; reproduces the oracle [4,6,14,32,...] (char 0)
                        and [4,9,17,35,...] (p = 2), and unlocks depth N >= 20.

These cover the single-cycle (truncated polynomial / cyclic Nakayama) and the
radical-square-zero overlap families, and the GENERAL overlap chains (cyclic
Nakayama with ell > 2, where relations overlap nontrivially) -- all oracle-exact.

------------------------------------------------------------------------------
SCOPE / FUTURE WORK
------------------------------------------------------------------------------
The differential above is implemented and verified for the families listed. The
general-overlap big/small alternation is implemented exactly as Bardzell's formula
and validated on overlapping relations (cyclic Nakayama ell > 2). A residual
direction is a careful audit of SIGNS for monomial algebras whose quiver has several
vertices with branching loops at a single vertex of mixed even/odd relation lengths,
which the present test families do not exercise; for the verified families the maps
are sign-correct (exact agreement on all primes, all overlap degrees).
"""

